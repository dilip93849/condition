day = int(input("Enter the day number:"))
if(day == 1):
    print("sunday")
elif(day == 2):
    print("monday")
elif(day == 3):
    print("tuesday")
elif(day == 4):
    print("wedneday")
elif(day == 5):
    print("thursday")
elif(day == 6):
    print("friday")
elif(day == 7):
    print("saturaday")
else:
    print("not valid:")
